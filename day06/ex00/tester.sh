#!/bin/bash

declare -a char=(# -- CHAR --
					"a"
					"Z"
					"~"
					" "
)

declare -a int=(# -- INT --
					"0"
					"+0"
					"-0"
					"0000"
					"-0000"
					"+0000"
					"-42"
					"+42"
					"32"
					"126"
					"-1234"
					"1234"
					"-2147483648"    # INT_MIN
					"2147483647"    # INT_MAX
)

declare -a float=(# -- FLOAT --
					"0f"
					"0.f"
					"0.0f"
					"0000f"
					"0000.f"
					"0.0000f"
					"0000.000f"
					"0000.001f"
					"4f"
					"4.f"
					"4.0f"
					"4.21f"
					"421.3321f"
					"4.99999999999999999999999999999999999999999999999999999999999999999f"
					".12f"
					".88f"
					"nanf"
					"+inff"
					"-inff"
)

declare -a double=(# -- DOUBLE --
					"0"
					"0."
					"0.0"
					"0000"
					"0000."
					"0.0000"
					"0000.000"
					"4"
					"4."
					"4.0"
					"4.21"
					"421.3321"
					"4.99999999999999999999999999999999999999999999999999999999999999999"
					"nan"
					"+inf"
					"-inf"
)

declare -a error=(#    -- ERROR --
					"-2147483649"    # INT_MIN -1
					"2147483648"    # INT_MAX + 1
					"az"
					"+az"
					"a+az"
					"+1+"
					"++"
					"++1"
					".++"
					".0q"
					"4..21"
					"4.21."
					"4.2.1"
					"ff"
					"f."
					"f0"
					"f42."
					"f42.0"
					"inf"
)

declare -a inputs=( "${char[@]}" "${int[@]}" "${float[@]}" "${double[@]}" "${error[@]}" )

for input in "${inputs[@]}"
do
	echo
	echo "-- \"$input\" --"
	./convert "${input}"
done
